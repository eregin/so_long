#include "libft.h"

void	*ft_bzero(void *dest, size_t n)
{
	size_t			i;
	unsigned char	*buf;

	i = 0;
	buf = dest;
	while (i < n)
	{
		buf[i] = '\0';
		i++;
	}
	return (dest);
}
